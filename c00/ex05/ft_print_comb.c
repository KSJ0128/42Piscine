#include <unistd.h>

void	ft_print_comb(void)
{
	char	num[4];

	num[0] = '0';
	while (num[0] < '8')
	{
		num[1] = num[0] + 1;
		while (num[1] > num[0] && num[1] < '9')
		{
			num[2] = num[1] + 1;
			while (num[2] > num[1] && num[2] <= '9')
			{
				if (num[0] < '7')
				{
					write(1, &num[0], 3);
					write(1, &(", "), 2);
				}
				num[2] += 1;
			}
			num[1] += 1;
		}
		num[0] += 1;
	}
	write(1, &("789"), 3);
}
