#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*new_str;
	unsigned int	s_len;
	unsigned int	i;

	s_len = ft_strlen(s);
	if (s_len == 0)
		return (0);
	new_str = (char *)malloc(s_len + 1);
	i = 0;
	while (s[i] != '\0')
	{
		new_str[i] = f(i, s[i]);
		i++;
	}
	return (new_str);
}
