void	ft_rev_int_tab(int *tab, int size)
{
	int	change;
	int	i;

	i = -1;
	while (++i < (size / 2))
	{
		change = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = change;
	}
	i = 0;
}
